package pojo;

public class BranchAddress {
    public String address1, address2, city, state, zip, country;

    public BranchAddress(String a1, String a2, String city, String state, String zip, String country) {
        this.address1 = a1;
        this.address2 = a2;
        this.city = city;
        this.state = state;
        this.zip = zip;
        this.country = country;
    }
}
